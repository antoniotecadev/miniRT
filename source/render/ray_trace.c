#include "../../include/minirt.h"

t_color	ray_trace(t_scene *scene, t_vec3 origin, t_vec3 dir)
{
	(void)scene;
	(void)origin;
	return ((t_color){
		(int)((dir.x + 1.0) * 127.5),
		(int)((dir.y + 1.0) * 127.5),
		(int)((dir.z + 1.0) * 127.5)
	});
}
